#Tipo de dato entero o largo
num = 15
print(num, type(num))

#Tipo de dato flotante
num_flot = 15.5
print(num_flot, type(num_flot))

#Tipo de dato Numero complejo
num_com = 5 + 6j
print(num_com, type(num_com))

#Tipo de dato String
nom = "Javier"
print(nom, type(nom))

#Tipo de dato booleano
ver_fal = 3 == 3
print(ver_fal, type(ver_fal))
