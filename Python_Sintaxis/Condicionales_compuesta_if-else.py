print("Convertido de numeros a letras")
num = int(input("Cual es el numero que deas convertir: "))
if num == 1:
    print("El numero es Uno")
elif num == 2:
    print("El numero es Dos")
elif num == 3:
    print("El numero es tres")
elif num == 4:
    print("El numero es Cuatro")
elif num == 5:
    print("El numero es Cinco")
else:
    print("Solo se puede convertit hasta el numero cinco")
